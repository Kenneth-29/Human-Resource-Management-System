    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        searchArea2.setEditable(false);
        
        try {
        //Open File and read records into list
        io = new InputOutput();
        io.openFile();
        records = io.readIntoArray();
        io.closeFile();
            
        search2 = new Search();
        
        resultsCity = new ArrayList<>();

        if(cityField.getText().equals(""))
        {
            searchArea2.setText("<<Enter a valid City name to search.");
        }else
        {
            String city = cityField.getText();
            
            resultsCity = search2.searchByCity(city, records, resultsCity);
            
            if(resultsCity.isEmpty())
            {
                searchArea2.setText("<<No results found for entered city.");
            }
            
            searchArea2.setText("ID\tName\tSurname");
            searchArea2.setText(searchArea2.getText()+"\n====================================\n");
            //Output Search result:
            for(Employee emp:resultsCity)
            {
                searchArea2.setText(searchArea2.getText()+emp.getEmployeeId()+"\t"+emp.getFirstName()+"\t"+emp.getLastName()+"\n");
            }
        
            cityCount.setText(""+resultsCity.size());
        }
          
        } 
        catch (IOException ex) 
        
        {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

