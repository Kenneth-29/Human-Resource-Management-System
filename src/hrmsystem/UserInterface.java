    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        salaryArea.setEditable(false);
        
        try {
            //Open File and read records into list
            io = new InputOutput();
            io.openFile();
            records = io.readIntoArray();
            io.closeFile();
            
            if(idField.getText().equals(""))
            {
                salaryArea.setText("<<Please enter a valid id of type int to search for an employee.");   
            }else
            {
                int id = Integer.parseInt(idField.getText());
            
            search = new Search();
            
            int index = search.searchEmployeeById(id, records);
            
            if(index == -1)
            {
                salaryArea.setText("<<No result found for ID "+id+".");   
            }else
            {
                Employee emp = records.get(index);
                
                salaryArea.setText("Employee ID: "+id); 
                salaryArea.setText(salaryArea.getText()+"\n=============\n"); 
                
                salaryArea.setText(salaryArea.getText()+"Name: "+emp.getFirstName());
                salaryArea.setText(salaryArea.getText()+"\nSurname: "+emp.getLastName());
                salaryArea.setText(salaryArea.getText()+"\nDepartment: "+emp.getDEPARTMENT());
                salaryArea.setText(salaryArea.getText()+"\nPosition: "+emp.getTitleOfEmployment()+"\n");
                
                salaryArea.setText(salaryArea.getText()+"\nBase");
                salaryArea.setText(salaryArea.getText()+"\n-------");
                salaryArea.setText(salaryArea.getText()+"\nHours Worked:\t\t"+emp.getHoursWorked()+" hrs");
                salaryArea.setText(salaryArea.getText()+"\nPay Rate:\t\t"+String.format("%.2f",emp.getRateOfPay())+"( per hour)");
                salaryArea.setText(salaryArea.getText()+"\nBase Salary:\t\t"+"P"+String.format("%.2f",emp.getSalary()));
                
                salaryArea.setText(salaryArea.getText()+"\n\nIncentives");
                salaryArea.setText(salaryArea.getText()+"\n-------------");
                salaryArea.setText(salaryArea.getText()+"\nCar Allowance:\t\t"+"P"+String.format("%.2f",emp.getCarAllowance())+" ("+emp.getCarAllowanceRate()+"%)");
                salaryArea.setText(salaryArea.getText()+"\nGratuity:\t\t"+"P"+String.format("%.2f",emp.getMonthlyGratuity()));
                
                salaryArea.setText(salaryArea.getText()+"\n\nTotal:\t\t"+"P"+String.format("%.2f",emp.calculateGrossSalary()));
            
            }
   
            }
            
        } catch (IOException ex) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

